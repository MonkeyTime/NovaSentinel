def normalize_language(language: str | None) -> str:
    if language in LANGUAGES:
        return str(language)
    return DEFAULT_LANGUAGE


def tr(language: str | None, key: str, **kwargs) -> str:
    language = normalize_language(language)
    text = TRANSLATIONS.get(language, {}).get(key, TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key))
    if kwargs:
        return text.format(**kwargs)
    return text


def trust_center_summary(language: str | None) -> str:
    return tr(language, "trust.summary")
